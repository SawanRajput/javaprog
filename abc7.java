public class abc7 {
    public static void main(String[] args) {
        int value1 = 1;
        int value2 = 2;

        if(value1 == value2)
        System.out.println("value1 == value2");

        if(value1 != value2)
        System.out.println("value1 != value2");

        if(value1 > value2)
        System.out.println("value1 > value2");

        if(value1 < value2)
        System.out.println("value1 < value2");

        if(value1 >= value2)
        System.out.println("value1 >= value2");

        if(value1 <= value2)
        System.out.println("value1 <= value2");

        if((value1 == 1) && (value2 == 2));
        System.out.println("value is 1 and value2 is 2");

        if((value1 == 1) || (value2 == 2));
        System.out.println("value is 1 or value2 is 2");
    }
}
